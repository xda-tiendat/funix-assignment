package com.datbt.error;

/**
 *
 * @author xdati
 */
public class ErrorDTO {
    String errFName;
    String errAge;
    String errCourse;

    public ErrorDTO() {
    }

    public String getErrFName() {
        return errFName;
    }

    public void setErrFName(String errFName) {
        this.errFName = errFName;
    }

    public String getErrAge() {
        return errAge;
    }

    public void setErrAge(String errAge) {
        this.errAge = errAge;
    }

    public String getErrCourse() {
        return errCourse;
    }

    public void setErrCourse(String errCourse) {
        this.errCourse = errCourse;
    }
    
    
}
